import fs from 'node:fs'
// Collect the icon names referenced in SECTIONS.
const sections = fs.readFileSync('src/lib/sections.ts', 'utf8')
const names = [...new Set([...sections.matchAll(/icon:\s*'([A-Za-z0-9]+)'/g)].map(m => m[1]))]
const kebab = (n) => n.replace(/([a-z0-9])([A-Z])/g, '$1-$2').replace(/([A-Z])([A-Z][a-z])/g, '$1-$2').replace(/([a-zA-Z])([0-9])/g, '$1-$2').toLowerCase()
const attrs = (o) => Object.entries(o).filter(([k]) => k !== 'key')
  .map(([k, v]) => `${k}="${v}"`).join(' ')
const out = {}
for (const name of names) {
  const file = `node_modules/lucide-react/dist/esm/icons/${kebab(name)}.js`
  if (!fs.existsSync(file)) { console.warn('missing', name, file); continue }
  const src = fs.readFileSync(file, 'utf8')
  const m = src.match(/createLucideIcon\(\s*"[^"]+",\s*(\[[\s\S]*?\])\s*\)/)
  if (!m) { console.warn('no node array', name); continue }
  // eslint-disable-next-line no-eval
  const nodes = eval(m[1])
  const inner = nodes.map(([tag, a]) => `<${tag} ${attrs(a)}/>`).join('')
  out[name] = inner
}
const header = `// AUTO-GENERATED by scripts/gen-section-icons.mjs — do not edit.\n// Inner SVG (24x24, stroke=currentColor) for each section's lucide icon,\n// keyed by the icon name used in lib/sections.ts.\n`
const body = `export const SECTION_ICON_INNER: Record<string, string> = ${JSON.stringify(out, null, 2)}\n`
fs.mkdirSync('src/generated', { recursive: true })
fs.writeFileSync('src/generated/sectionIcons.ts', header + body)
console.log('wrote src/generated/sectionIcons.ts with', Object.keys(out).length, 'icons:', Object.keys(out).join(', '))
